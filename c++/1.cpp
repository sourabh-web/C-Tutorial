// C++ program to sizes of data types
#include<iostream>
using namespace std;

int main()
{
	cout << "Size of char : " << sizeof(char) 
	<< " byte \n";
	cout << "Size of int : " << sizeof(int)
	<< " bytes \n";
	cout << "Size of short : " << sizeof(short int) 
	<< " bytes \n";
	cout << "Size of long : " << sizeof(long int) 
	<< " bytes \n";
	cout << "Size of signed long : " << sizeof(signed long int)
	<< " bytes \n";
	cout << "Size of unsigned long : " << sizeof(unsigned long int) 
	<< " bytes \n";
	cout << "Size of float : " << sizeof(float) 
	<< " bytes \n";
	cout << "Size of double : " << sizeof(double) 
	<< " bytes \n";
	cout << "Size of bool : " << sizeof(bool) 
	<< " bytes \n\n ";
	
	
	return 0;
}


